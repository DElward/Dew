            (p_curl_easy_strerror)dynloadlibcurlproc(hte->hte_dynl,
                libcurlhome, libcurlpath, libcurllib, "curl_easy_strerror",
                &dynerr, errmsg, sizeof(errmsg));
        if (!hte->hte_dcf->dcf_curl_easy_strerror) hte->hte_libcurl_resolved = -1;

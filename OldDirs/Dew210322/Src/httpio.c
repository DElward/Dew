    int hterr;
    char * libcurlhome;
    char * libcurlpath;
    char * libcurllib;
    char errmsg[256];

    hterr = 0;

    hte->hte_dcf  = dcf_new();
    hte->hte_dynl = dynl_dynloadnew();
    hte->hte_libcurl_resolved = 0;

    libcurlhome = get_dewvar("LIBCURL_HOME");
    libcurlpath = get_dewvar("DEWLIBCURLLIBPATH");
    libcurllib  = get_dewvar("DEWLIBCURLLIB");

    /* curl_easy_init */
    hte->hte_dcf->dcf_curl_easy_init =
        (p_curl_easy_init)dynloadlibcurlproc(hte->hte_dynl,
            libcurlhome, libcurlpath, libcurllib, "curl_easy_init",
            &dynerr, errmsg, sizeof(errmsg));
    if (!hte->hte_dcf->dcf_curl_easy_init) hte->hte_libcurl_resolved = -1;

    /* curl_easy_setopt */
    if (!hte->hte_libcurl_resolved) {
        hte->hte_dcf->dcf_curl_easy_setopt =
            (p_curl_easy_setopt)dynloadlibcurlproc(hte->hte_dynl,
                libcurlhome, libcurlpath, libcurllib, "curl_easy_setopt",
                &dynerr, errmsg, sizeof(errmsg));
        if (!hte->hte_dcf->dcf_curl_easy_setopt) hte->hte_libcurl_resolved = -1;
    }


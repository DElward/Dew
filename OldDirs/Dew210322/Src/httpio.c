int ht_set_request(
    struct htenv * hte,
    struct dynamic_curl * dcf,
    CURL *curl_handle,
    const char * request,
    int debug_flags)
{
/*
** 03/12/2020
** 05/05/2020: Added HEAD, PUT, DELETE, etc.
** 05/05/2020: See  https://en.wikipedia.org/
**                  wiki/Hypertext_Transfer_Protocol#Request_message
*/
    int hterr;
    CURLcode res;

    hterr = 0;

    if (debug_flags & HTE_DEBUG_SHOW_HEADERS) {
        printf("  http request \'%s\'\n", request);
    }

    if (!strcmp(request, "GET")) {
        res = dcf->dcf_curl_easy_setopt(curl_handle, CURLOPT_HTTPGET, 1);
    } else if (!strcmp(request, "POST")) {
        res = dcf->dcf_curl_easy_setopt(curl_handle, CURLOPT_POST, 1);
    } else if ( !strcmp(request, "HEAD"     ) ||    /* 05/05/2020 */
                !strcmp(request, "PUT"      ) ||    /* 05/05/2020 */
                !strcmp(request, "DELETE"   ) ||    /* 05/05/2020 */
                !strcmp(request, "CONNECT"  ) ||    /* 05/05/2020 */
                !strcmp(request, "OPTIONS"  ) ||    /* 05/05/2020 */
                !strcmp(request, "TRACE"    ) ||    /* 05/05/2020 */
                !strcmp(request, "PATCH"    ) ) {   /* 05/05/2020 */
        res = dcf->dcf_curl_easy_setopt(curl_handle, CURLOPT_CUSTOMREQUEST,
                    request);
    } else {
        hterr = ht_set_error(hte, HTE_EBADREQUEST,
                   "Invalid HTTP request: \'%s\'", request);
    }

    if (!hterr && res != CURLE_OK) {
        hterr = ht_set_error(hte, HTE_EBADREQUEST,
                    "Error %d on set request %s: %s",
                    res, request, dcf->dcf_curl_easy_strerror(res));
    }

    return (hterr);
}

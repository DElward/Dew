struct curl_slist * htf_set_http_headers(
    struct dynamic_curl * dcf,
    const char * httphdrs,
    int debug_flags)
{
/*
** Fills http header struct curl_slist from httphdrs.
** Multiple headers are separated in httphdrs by '\n'
*/
    char * nl;
    const char * hp;
    char * hdr;
    int hdrlen;
    struct curl_slist * slist;

    slist = NULL;
    hp = httphdrs;
    while (hp[0]) {
        nl = strchr(hp, '\n');
        if (!nl) hdrlen = IStrlen(hp);
        else     hdrlen = (int)(nl - hp);

        if (hdrlen) {
            hdr = New(char, hdrlen + 1);
            memcpy(hdr, hp, hdrlen);
            hdr[hdrlen] = '\0';
            if (debug_flags & HTE_DEBUG_SHOW_HEADERS) {
                printf("  http header \'%s\'\n", hdr);
            }

            slist = dcf->dcf_curl_slist_append(slist, hdr);
            Free(hdr);
            hp += hdrlen;
        }
        if (nl) hp++;   /* 02/19/2020 - skip \n */
    }

    return (slist);
}

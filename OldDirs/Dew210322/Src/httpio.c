/*@*/intr httpfile_fread(struct httpfile * htf,
                      void * buffer,
                      intr size,
                      EBLK ** err) {
/*
** Returns number of bytes read or 0 on end-of-file.
** Sets *err if any other error occurs.
*/
    int avail;
    int nread;

    nread = 0;
    avail = htf->htf_databufsiz - htf->htf_databufix;
    if (avail > size) nread = (int)size;
    else              nread = avail;

    if (nread) {
        memcpy(buffer, htf->htf_databuf + htf->htf_databufix, nread);
        htf->htf_databufix += nread;
    }

    return (nread);
}

int htf_curl_get_file(struct httpfile * htf,
        const char * url,
        const char * httphdrs)
{
    CURL *curl_handle;
    CURLcode res;
    int hterr;
    int debug_flags;
    struct dynamic_curl * dcf;
    struct curl_slist * slist;

    hterr = 0;
    debug_flags = htf->htf_hte->hte_debug_flags;    /* 02/19/2020 */

    dcf = htf->htf_hte->hte_dcf;

    curl_handle = initialize_curl(dcf, debug_flags);    /* 05/05/2020 */
    if(!curl_handle) {
        hterr = ht_set_error(htf->htf_hte, HTE_ECURLINIT,
                   "Unexpected error calling curl_easy_init()");
    } else {
        dcf->dcf_curl_easy_setopt(curl_handle, CURLOPT_URL, url);

        slist = NULL;
        if (httphdrs) {
            slist = htf_set_http_headers(dcf, httphdrs, debug_flags);
            if (slist) {
                dcf->dcf_curl_easy_setopt(curl_handle,
                    CURLOPT_HTTPHEADER, slist);
            }
        }

        /* we tell libcurl to follow redirection */
        dcf->dcf_curl_easy_setopt(curl_handle, CURLOPT_FOLLOWLOCATION, 1L);

        /* 06/04/2018 */
        /*    Required for:  https://www.travelocity.com/ */
        /* enable all supported built-in compressions */
        dcf->dcf_curl_easy_setopt(curl_handle, CURLOPT_ACCEPT_ENCODING, "");

        /* send all data to this function  */
        dcf->dcf_curl_easy_setopt(curl_handle,
            CURLOPT_WRITEFUNCTION, curl_WriteMemoryCallback);

        /* we pass our 'chunk' struct to the callback function */
        dcf->dcf_curl_easy_setopt(curl_handle, CURLOPT_WRITEDATA, (void *)htf);

        /* some servers don't like requests that are made without a user-agent
            field, so we provide one */
        dcf->dcf_curl_easy_setopt(curl_handle,
            CURLOPT_USERAGENT, WH_CURL_AGENT);

        /* Perform the request, res will get the return code */
        res = dcf->dcf_curl_easy_perform(curl_handle);
        /* Check for errors */
        if(res != CURLE_OK) {
            hterr = ht_set_error(htf->htf_hte, HTE_ECURLPROCESS,
                       "Error %d on perform: %s",
                        res, dcf->dcf_curl_easy_strerror(res));
        }

        /* always cleanup */
        dcf->dcf_curl_easy_cleanup(curl_handle);
		if (slist) dcf->dcf_curl_slist_free_all(slist);
    }

    return (hterr);
}

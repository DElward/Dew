static int htf_read_callback(
            struct httpfile * htf,
            char * data_read,
            int    data_size)
{
    if (htf->htf_databufsiz + data_size >= htf->htf_databufmax) {
        htf->htf_databufmax =
            (htf->htf_databufmax + data_size + 1 + WH_DATA_PAGE_INCREMENT) &
                WH_DATA_PAGE_MASK;
        htf->htf_databuf = Realloc(htf->htf_databuf, char, htf->htf_databufmax);
    }
    memcpy(htf->htf_databuf + htf->htf_databufsiz, data_read, data_size);
    htf->htf_databufsiz += data_size;
    htf->htf_databuf[htf->htf_databufsiz] = '\0';

    return (data_size);
}
